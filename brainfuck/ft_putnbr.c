#include "ftlib.h"

void	ft_putnbr(int i)
{
	if (i < 0)
	{
		ft_putchar('-');
	}
	if (i >= 10)
	{
		ft_putnbr(i / 10);
		ft_putnbr(i % 10);
	}
	else
	{
		ft_putchar(i + 48);
	}
}
